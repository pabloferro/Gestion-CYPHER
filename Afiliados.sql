DROP PROCEDURE CYPHER.GETAFILIADOS

GO

CREATE PROCEDURE CYPHER.GETAFILIADOS(@nombre nvarchar(255), @apellido nvarchar(255))
AS
BEGIN
	SELECT AFIL_NROAFILIADO Numero, AFIL_APELLIDO Apellido, AFIL_NOMBRE Nombre, AFIL_DNI DNI, AFIL_PLAN 'Plan'
	FROM CYPHER.AFILIADO
	WHERE AFIL_NOMBRE like '%'+@nombre+'%' AND AFIL_APELLIDO like '%'+@apellido+'%' 
END